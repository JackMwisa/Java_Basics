package Day1;

public class FirstJavaProgram {
    public static void main(String[] args) {

        //using the println method
        System.out.println("Good Morning!!");
        System.out.println("I am called Jack");
        System.out.println("How are you??");
        System.out.println("What is your name?");
        System.out.println("\n");

        //using print method
        System.out.print("Hello");
        System.out.print("Mark\n\n");


        //using println for numbers
        // for numbers we do not use quotes
        System.out.println(89);
        System.out.println(132);
        System.out.println(44532 + "\n");

        //mathematical calculation in print method
        System.out.println("addition: 7 + 2 = " + 7 + 2);
        System.out.println("substraction: 7 - 3 = " + (7 - 3));
        System.out.println("multiplication: 7 * 4 = " + 7 * 4);
        System.out.println("division: 7 / 2 = " + 7 / 3);

    }
}


